//? Type Casting
import java.util.*;
public class Type_Casting
{
    public static void main(String args[])
    {
        Scanner sc=new Scanner(System.in);
        float a=12.98f;
        int b=(int)a;
        System.out.println(b);
        char ch='a';
        int c=(int)ch;
        System.out.println(c);
    }
}